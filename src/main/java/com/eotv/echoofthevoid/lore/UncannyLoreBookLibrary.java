    private UncannyLoreBookLibrary() {
    }

    public static int volumeCount() {
        return VOLUME_COUNT;
    }

    public static int clampVolume(int volume) {
        if (volume < 1) {
            return 1;
        }
        return Math.min(volume, VOLUME_COUNT);
    }

    public static WrittenBookContent contentForVolume(int volume) {
        int clamped = clampVolume(volume);
        String title = "Uncanny Lore Piece Vol." + clamped;
        List<Filterable<Component>> pages = pagesFromText(VOLUMES[clamped - 1]);
        return new WrittenBookContent(
                Filterable.passThrough(title),
                "Unknown",
                0,
                pages,
                true);
    }

    public static WrittenBookContent defaultContent() {
        return contentForVolume(1);
    }

    private static List<Filterable<Component>> pagesFromText(String text) {
        String normalized = text.replace("\r\n", "\n").trim();
        List<Filterable<Component>> pages = new ArrayList<>();
        for (String block : normalized.split("\\n\\n")) {
            String page = block.strip();
            if (!page.isEmpty()) {
                pages.add(Filterable.passThrough(Component.literal(page)));
            }
        }
        if (pages.isEmpty()) {
            pages.add(Filterable.passThrough(Component.literal("Unreadable notes.")));
        }
        return List.copyOf(pages);
    }
